// Function to generate additional trends to fill the list
export function generateAdditionalTrends(platform, month, existingCount) {
  const categories = {
    instagram: ["Lifestyle", "Fashion", "Food", "Travel", "Art", "Fitness", "Beauty"],
    twitter: ["Politics", "Sports", "Entertainment", "Technology", "News", "Social"],
    google: ["Technology", "Entertainment", "Shopping", "Health", "Travel", "Education"],
    youtube: ["Music", "Gaming", "Comedy", "Education", "Vlogs", "Sports"],
  }

  const engagementRanges = {
    instagram: { min: 100000, max: 1000000 },
    twitter: { min: 500000, max: 2000000 },
    google: { min: 1000000, max: 5000000 },
    youtube: { min: 2000000, max: 10000000 },
  }

  return Array.from({ length: Math.max(0, 25 - existingCount) }, (_, i) => ({
    id: `${platform}_${month}_${i + existingCount + 1}`,
    name: `#${platform.charAt(0).toUpperCase() + platform.slice(1)}Trend${i + existingCount + 1}`,
    platform: platform,
    date: month + "-15",
    category: categories[platform][i % categories[platform].length],
    engagement:
      Math.floor(Math.random() * (engagementRanges[platform].max - engagementRanges[platform].min)) +
      engagementRanges[platform].min,
    rank: i + existingCount + 1,
    posts: [
      {
        id: `${platform}_post_${month}_${i + existingCount + 1}`,
        username: `${platform}_user_${i + 1}`,
        date: month.split("-")[1] + " " + month.split("-")[0],
        content: `Sample content for trend ${i + existingCount + 1}`,
        likes: Math.floor(Math.random() * 100000),
        shares: Math.floor(Math.random() * 20000),
        comments: Math.floor(Math.random() * 5000),
        url: `https://${platform}.com/example${i + existingCount + 1}`,
        ...(platform === "youtube" && {
          thumbnail: `https://via.placeholder.com/320x180/cccccc/333333?text=Video${i + existingCount + 1}`,
        }),
      },
    ],
  }))
}
